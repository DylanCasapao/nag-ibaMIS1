1.1 History of Barangay Nag-iba 
<br>
<br>

Just like any other things on earth it has its own beginning, existence, struggle and end. Nag-iba is one of the remote <br> barrios of Mabini, once a sitio of Pulong Niogan a barrio of our mother town Bauan has her own social and political identity. It is the home of industrious, religious and peace-loving people.

Nag-iba was one of the big barrios that composed out town Mabini. It was bordered by Bagalangit on the north, Batangas Bay on the south, Malimatoc on the east, and the China Sea on the west. This place is in the southern part of Mabini and is mountainous. It is composed of nine (9) sitios namely: Panay, Balanoy, Mainit, Saimsin, Bangon, Nangkaan, Caballero, Bukal and Naambon.

As to why the place was called Nag-iba folk tale has it. The place was formerly a part of Bagalangit and Malimatoc. Because of the uncommon denominator of the people that reside therein planned and created a now barrio under the leadership of one Lino Dipasupil where then became the first Cabeza de Barangay of the place. The newly created barrio was different from the twin Mother Barrios. Folks called the new barrio Nag-iba which means indifference thus became the origin and foundation of Barangay Nag-iba.

Because of the social, political dimension and of different aspects the residents of the western portion of the barrio after several years filed a resolution asking for the creation of another barrio. It was under the leadership of Mr. Teodoro Mendoza, It was granted and approved under the resolution No. 2 as 1964 and was named San Teodoro during the incumbency of Barrio Leiutenant Basilio Magnaye a long time political big boss of the place. Under the same circumstances and of common factors came the creation of another barrio from Nag-iba called Mainit, under the leadership of Mr. Bonifacio Ilagan. It was approved on July 1, 1972 during the incumbency of the young Barrio Captain Mr. Vicente Magnaye the son of former Barrio Lieutenant Basilio Magnaye. Just after his term of office young Magnaye resigned from his political career in his place, giving way to Mr. Felixberto Bueno who then was elected Barangay Captain of the place. Under his administration a three (3) kms. feader road more or less was constructed from barangay Laurel, and Barangay Malimatoc to Nag-iba Elementary School. Spring development and other water system were constructed too.

The first public school in Nag-iba was opened in the sitio of Naambon with Mr. Aseron the first teacher. It was during the incumbency of Cabeza de Barangay Pedro Manalo. It did not last long in the said place. Later another public school was opened located at the sitio of Bukal through the able leadership of Barrio Lieutenant Juan de Rosales. Classes were held in private building hired by the government. Mr. Aniceto P. Jusi was the first teacher in that school under the principal of the Central School. Then later a new school building was created on a hill sitio of Caballero. The site of which was donated by a civic minded Victoriano Vizco of the barrio with no string attach.

Not later after, Mr. Felixberto Bueno resigned from his position. Then he was succeeded again by Mr. Vicente Magnaye, appointed, Barangay Captain by the newly elected Mayor Ruben R. Amurao.

Three years after, Mr. Vicente Magnaye resigned from his position because he was appointed as Municipal Secretary by Mayor Ruben R. Amurao. So then, Mr. Macario Castillo, the first councilor, was appointed as Barangay Captain of Nag-iba, up to the present.

Under his administration another three (3) kms. road more or less was constructed from Barangay Malimatoc II to Nag-iba Elementary School. Reads from each sitio was opened which is of great help to the people living in the community whenever transportation is accessible. The appointed Barangay Captain Macario Castillo was then elected as Barangay Captain during the Barangay Election last March 28, 1989.
Thru his able leadership, BATELEC II at Barangay Nag-iba was
Organized and constructed. Sooner electric power in the said barangay will energize.

Another construction was finished, a concrete road with 120 meters long, 3 meters wide and with a thickness of 6 inches.
The writer de hope that Nag-iba with his future young, dynamic, energetic, talented and able leader will became great again.

